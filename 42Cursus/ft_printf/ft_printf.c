#include "ft_printf.h"
#include "Libft/libft.h"

int		ft_printf(const char *formats, ...)
{
	t_tab *tab;

	if (!(tab = (t_tab*)malloc(sizeof(t_tab))))
		return (-1);
	tab->formats = formats;
	tab = tab_initializer(tab);
	if (formats)
	{
		va_start(tab->punt_arg, formats);
		processor(tab);
		va_end(tab->punt_arg);
	}
	free(tab);
	return (tab->len);
}

