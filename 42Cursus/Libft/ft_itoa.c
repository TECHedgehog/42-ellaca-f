#include <stdlib.h>

char	*ft_itoa(int n)
{
	int		j;
	int		l;
	char	*num;

	j = (n > 0) ? 0 : 1;
	l = (n < 0) ? n * -1 : n;
	while (l > 0)
	{
		l /= 10;
		j++;
	}
	l = (n < 0) ? n * -1 : n;
	num = malloc(j + 1);
	if (!num)
		return (0);
	num[j] = '\0';
	num[0] = (n == 0) ? '0' : '0';
	while (l > 0)
	{
		num[j - 1] = l % 10 + 48;
		l /= 10;
		j--; 
	}
	(n < 0) ? num[0] = '-' : 0;
    return (num);
}